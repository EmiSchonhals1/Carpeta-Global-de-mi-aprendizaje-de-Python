#Nos permite mostrar en una cadena o iterable desde el índice que queremos que inicie hasta el índice que queremos 
# que termine (sin incluir este último)

lista = ["Emi", "Marcos", "Raúl", "Uli", "José"]

print(lista[0:3])
#en este caso nos muestra del índice 0 al índice 2 (ya que el límite superior no se toma)

cadena = "123456789"

print(cadena[2:5])
#en este caso nos muestra del índice 2 al índice 4 (ya que el límite superior no se toma)




