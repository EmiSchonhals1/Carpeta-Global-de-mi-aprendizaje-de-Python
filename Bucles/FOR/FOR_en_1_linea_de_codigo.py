lista = [2, 4, 6, 8, 10]

#multiplicamos por 2 sus elementos y los mostramos en UNA SOLA LÍNEA DE CÓDIGO
resultado = [numero*2 for numero in lista]
print (resultado)

#le restamos 2 a cada elemento y los mostramos en UNA SOLA LÍNEA DE CÓDIGO
resultado_resta = [numero-2 for numero in lista]
print (resultado_resta)