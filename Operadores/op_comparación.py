print ("Contamos con los enteros 5 y 6")

a = 5
b = 6

#LOS OPERADORES DE COMPARACIÓN SIEMPRE NOS DAN VALORES TRUE O FALSE
igual_que = a == b
distinto_que = a != b
mayor_que = a > b
menor_que = a < b
mayor_o_igual_que = a >= b
menor_o_igual_que = a <= b

print (igual_que)
print (distinto_que)
print (mayor_que)
print (menor_que)
print (mayor_o_igual_que)
print (menor_o_igual_que)

#podemos hacer operaciones combinadas con los operadores de comparación
c = 11

comparacion = a + b == c
print ("5 + 6 es igual a 11?")
